package day13_String;

public class ChangingCases {

	public static void main(String[] args) {
		
		
		String str = "Hello";
		System.out.println(str);   // Hello
		
		str = str.toLowerCase();
		System.out.println(str);   // hello
		
		str = str.toUpperCase();
		System.out.println(str);   // HELLO
		
		String str2 = "Nicky";
		System.out.println(str2);   // Nicky

	}

}
